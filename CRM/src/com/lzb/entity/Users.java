package com.lzb.entity;

public class Users {

	private Integer Uid;
	private String LoginName;
	private String PassWord;
	private Integer IsLoginData;
	private String LastLoginData;
	private String CreateData;
	private String Email;
	private Integer Mtel;
	private Integer SignIn;
	private String SignInData;
	public Integer getUid() {
		return Uid;
	}
	public void setUid(Integer uid) {
		Uid = uid;
	}
	public String getLoginName() {
		return LoginName;
	}
	public void setLoginName(String loginName) {
		LoginName = loginName;
	}
	public String getPassWord() {
		return PassWord;
	}
	public void setPassWord(String passWord) {
		PassWord = passWord;
	}
	public Integer getIsLoginData() {
		return IsLoginData;
	}
	public void setIsLoginData(Integer isLoginData) {
		IsLoginData = isLoginData;
	}
	public String getLastLoginData() {
		return LastLoginData;
	}
	public void setLastLoginData(String lastLoginData) {
		LastLoginData = lastLoginData;
	}
	public String getCreateData() {
		return CreateData;
	}
	public void setCreateData(String createData) {
		CreateData = createData;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public Integer getMtel() {
		return Mtel;
	}
	public void setMtel(Integer mtel) {
		Mtel = mtel;
	}
	public Integer getSignIn() {
		return SignIn;
	}
	public void setSignIn(Integer signIn) {
		SignIn = signIn;
	}
	public String getSignInData() {
		return SignInData;
	}
	public void setSignInData(String signInData) {
		SignInData = signInData;
	}
	
}
